# Crear 2 tuplas con valores numéricos y mostrar la multiplicación de sus elementos en una nueva tupla

tuplaU = (4, 8, 12)
tuplaD = (3, 6, 9)

tuplaSum = (tuplaU[0] + tuplaD[1])
tuplaRes = (tuplaU[1] - tuplaD[2])
tuplaMulti = (tuplaU[2] * tuplaD[0])
tuplaDiv = (tuplaU[0] / tuplaD[1])

print("Suma: ", tuplaSum)
print("Resta: ", tuplaRes)
print("Multiplicación: ", tuplaMulti)
print("División: ", tuplaDiv)